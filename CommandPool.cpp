CommandPool::CommandPool(Device* device,SwapChain* swapchain) : device(device), swapchain(swapchain) {
  createCommandPool();
}

CommandPool::~CommandPool() {
  vkDestroyCommandPool(device->getLogical(), commandPool, nullptr);
}

VkCommandPool CommandPool::getCommandPool() const {
  return commandPool;
}

void CommandPool::createCommandPool() {
    QueueFamilyIndices queueFamilyIndices = device->findQueueFamilies(device->getPhysical());

    VkCommandPoolCreateInfo poolInfo{};
    poolInfo.sType = VK_STRUCTURE_TYPE_COMMAND_POOL_CREATE_INFO;
    poolInfo.flags = VK_COMMAND_POOL_CREATE_RESET_COMMAND_BUFFER_BIT;
    poolInfo.queueFamilyIndex = queueFamilyIndices.graphicsFamily.value();

    if (vkCreateCommandPool(device->getLogical(), &poolInfo, nullptr, &commandPool) != VK_SUCCESS) {
      throw std::runtime_error("failed to create graphics command pool!");
    }
  }

